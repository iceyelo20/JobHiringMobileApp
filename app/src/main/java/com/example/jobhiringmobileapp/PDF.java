package com.example.jobhiringmobileapp;

public class PDF {

    public String name, url, request_type, uid, date, time, timestamp, jobId;

    public PDF() {
    }

    public PDF(String name, String url, String request_type, String uid, String date, String time, String timestamp, String jobId) {
        this.name = name;
        this.url = url;
        this.request_type = request_type;
        this.uid = uid;
        this.date = date;
        this.time = time;
        this.timestamp = timestamp;
        this.jobId = jobId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getRequest_type() {
        return request_type;
    }

    public void setRequest_type(String request_type) {
        this.request_type = request_type;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getTimestamp() {
        return timestamp;
    }

    public void setTimestamp(String timestamp) {
        this.timestamp = timestamp;
    }

    public String getJobId() {
        return jobId;
    }

    public void setJobId(String jobId) {
        this.jobId = jobId;
    }
}
